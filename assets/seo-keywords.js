window.generateTeamKeywords = function(teamName) {
  const base = window.BRAZILIAN_FOOTBALL_KEYWORDS;
  const teamKeys = base.teamKeywords[teamName] || [];
  
  return [
    ...teamKeys,
    ...base.timeKeywords.map(time => `${teamName.toLowerCase()} ${time}`),
    ...base.actionKeywords.map(action => `${action} ${teamName.toLowerCase()}`),
    ...base.platforms.map(platform => `${teamName.toLowerCase()} ${platform}`)
  ];
};